def translate_string(vi_str):
    """Translate a Vietnamese string to English using the dictionary."""
    # Exact match first
    if vi_str in TRANSLATIONS:
        return TRANSLATIONS[vi_str]

    # Try with unescaped quotes
    unescaped = vi_str.replace('\\"', '"')
    if unescaped in TRANSLATIONS:
        return TRANSLATIONS[unescaped]

    # Try without emoji prefix
    stripped = re.sub(r'^[📅⚠️🔍📊★→✓⏳🔄❌✅📋🚨↩📱✂🏷✗📈🚚📦]+\s*', '', vi_str)
    if stripped in TRANSLATIONS:
        # Keep the emoji, translate the text
        prefix = vi_str[:len(vi_str) - len(stripped)]
        return prefix + TRANSLATIONS[stripped]

    return None


def process_pot_file(pot_path, po_path, module_name):
    """Read a .pot file and create an en.po file with translations."""
    with open(pot_path, 'r', encoding='utf-8') as f:
        content = f.read()

    # Update header for English
    header_replacements = {
        '"Language-Team: \\n"': '"Language-Team: English\\n"',
        '"Plural-Forms: \\n"': '"Plural-Forms: nplurals=2; plural=(n != 1);\\n"',
        '"Content-Transfer-Encoding: \\n"': '"Content-Transfer-Encoding: 8bit\\n"',
    }
    for old, new in header_replacements.items():
        content = content.replace(old, new)

    # Add Language header after MIME-Version line
    content = content.replace(
        '"MIME-Version: 1.0\\n"',
        '"MIME-Version: 1.0\\n"\n"Language: en\\n"'
    )

    # Process each msgid/msgstr pair
    lines = content.split('\n')
    result = []
    i = 0
    translated_count = 0
    total_vi_count = 0

    while i < len(lines):
        line = lines[i]

        # Detect msgid line
        if line.startswith('msgid "') and not line == 'msgid ""':
            # Extract the full msgid (may span multiple lines)
            msgid_text = line[7:-1]  # Remove 'msgid "' and trailing '"'

            # Check for multi-line msgid
            result.append(line)
            i += 1
            while i < len(lines) and lines[i].startswith('"') and not lines[i].startswith('msgstr'):
                msgid_text += lines[i][1:-1]
                result.append(lines[i])
                i += 1

            # Now we should be at the msgstr line
            if i < len(lines) and lines[i].startswith('msgstr "'):
                existing_msgstr = lines[i][8:-1]

                # Check if this msgid contains Vietnamese characters
                has_vietnamese = bool(re.search(
                    r'[àáảãạăắằẳẵặâấầẩẫậèéẻẽẹêếềểễệìíỉĩịòóỏõọôốồổỗộơớờởỡợùúủũụưứừửữựỳýỷỹỵđÀÁẢÃẠĂẮẰẲẴẶÂẤẦẨẪẬÈÉẺẼẸÊẾỀỂỄỆÌÍỈĨỊÒÓỎÕỌÔỐỒỔỖỘƠỚỜỞỠỢÙÚỦŨỤƯỨỪỬỮỰỲÝỶỸỴĐ]',
                    msgid_text
                ))

                if has_vietnamese:
                    total_vi_count += 1
                    translation = translate_string(msgid_text)
                    if translation:
                        result.append('msgstr "%s"' % translation)
                        translated_count += 1
                    else:
                        # For untranslated Vietnamese strings, set msgstr empty
                        # Odoo will fall back to msgid
                        result.append(lines[i])
                else:
                    result.append(lines[i])
                i += 1
            continue

        result.append(line)
        i += 1

    with open(po_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(result))

    return translated_count, total_vi_count


def main():
    total_translated = 0
    total_vietnamese = 0

    for pot_path in sorted(glob.glob(os.path.join(ADDONS_PATH, '*/i18n/*.pot'))):
        module_dir = os.path.dirname(os.path.dirname(pot_path))
        module_name = os.path.basename(module_dir)
        i18n_dir = os.path.join(module_dir, 'i18n')
        po_path = os.path.join(i18n_dir, 'en.po')

        translated, vi_count = process_pot_file(pot_path, po_path, module_name)
        total_translated += translated
        total_vietnamese += vi_count
        pct = (translated / vi_count * 100) if vi_count > 0 else 0
        print('%s: %d/%d (%.0f%%)' % (module_name, translated, vi_count, pct))

    pct = (total_translated / total_vietnamese * 100) if total_vietnamese > 0 else 0
    print('\n=== TOTAL: %d/%d Vietnamese strings translated (%.0f%%) ===' % (
        total_translated, total_vietnamese, pct))


if __name__ == '__main__':
    main()
